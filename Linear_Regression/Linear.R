height <- c(103,117,105,141,115,138,144,137,100,131,119,115,121,113) 
weight <- c(61,46,62,54,60,51,50,46,64,48,56,64,48,59)
length(weight)
length(height)
student <- lm(weight ~ height)
summary(student)
new_data <- data.frame(height = 199)
predictions <- predict(student, newdata = new_data, interval = "confidence")
print(predictions)
plot(height, weight, main = "Linear Regression", xlab = "Height", ylab = "Weight")
abline(student, col = "red")
points(new_data$height, predictions[1], col = "blue", pch = 19)
arrows(new_data$height, predictions[2], new_data$height, predictions[3], angle = 90, code = 3, col = "blue")
